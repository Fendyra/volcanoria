                   <span class="section-title-informasi">1. Saat Bencana</span></h1>
                    <p class="justify-content-between">Hasil observasi bencana berupa letusan gunung berapi, tanah longsor, gempa bumi, dan daerah aliran lahar. Tempat tersebut dapat terhindar dari bahaya karena terletak relatif jauh dari daerah rawan. Perlindungan diri bisa dilakukan oleh:
                    <ol>
                        <li>Hindari Daerah Rawan Bencana</li>
                        <p>Hindari daerah rawan bencana seperti lereng gunung, lembah, dan daerah aliran lahar. Tempat terbuka dapat berbahaya karena lontaran material (pijar) dan awan panas.</p>
                        <li>Perlindungan diri dari Abu Letusan dan Awan Panas.</li>
                        <p>Ditempat terbuka, lindungi diri dari abu letusan dan awan panas. Persiapkan diri untuk kemungkinan bencana susulan. Kenakan pakaian yang bisa melindungi tubuh seperti baju lengan panjang, celana panjang, topi, dan lainnya. Jangan memakai lensa kontak.</p>
                        <li>Lindungi Saluran Pernapasan</li>
                        <p>Gunakan masker atau kain basah untuk menutupi hidung dan mulut agar terlindung dari abu vulkanik dan gas beracun yang dapat mengganggu pernapasan.</p>
                        <li>Evakuasi Darurat</li>
                        <p>Ikuti instruksi dari otoritas lokal untuk segera mengevakuasi diri dari zona berbahaya. Pindah ke tempat evakuasi yang telah ditentukan. Jika sedang berada di tempat terbuka, pergi ke tempat tinggi karena letusan besar diikuti aliran lava, lumpur, dan banjir. Walau sudah berada di tempat tinggi, tetap waspada dan lindungi diri dari muntahan bebatuan hingga gas dari erupsi.</p>
                        <li>Peringatan dan Informasi</li>
                        <p>Menggunakan sistem peringatan dini untuk memberikan informasi kepada penduduk tentang status gunung berapi dan tindakan yang harus dilakukan. Peringatan bisa disampaikan melalui sirine, SMS, radio, dan media sosial. Serta menyediakan pdate rutin mengenai situasi letusan, termasuk arah angin yang membawa abu vulkanik.</p>
                    </ol>
                    </p>
            
                   <span class="section-title-informasi">2. Pasca Bencana</span></h1>
                    <p class="justify-content-between">Jalur wilayah yang terkena hujan abu vulkanik harus bersih dari abu vulkanik bila mereka akan melakukan perjalanan atau kegiatan di luar ruangan.
                    Perlindungan diri bisa dilakukan oleh:
                    <ol>
                        <li>Jauhi Wilayah yang Terkena Hujan Abu</li>
                        <p>Jauhi wilayah yang terkena hujan abu karena beratnya abu vulkanik bisa merusak atau meruntuhkan atap bangunan.</p>
                        <li>Pembersihan dan Pemulihan Insfrastruktur dan Lingkungan</li>
                        <p>Segera lakukan pembersihan abu vulkanik dari jalan, rumah, dan fasilitas umum untuk mencegah kerusakan lebih lanjut dan memulihkan mobilitas masyarakat. Dan melakukan reboisasi dan rehabilitasi lahan yang rusak akibat letusan. Tanam kembali vegetasi yang hilang untuk mengurangi risiko longsor dan erosi.</p>
                        <li>Rekontruksi dan Rehabilitasi</li>
                        <p>Membangun kembali rumah, sekolah, dan infrastruktur yang rusak akibat letusan. Proses ini harus memperhitungkan potensi risiko bencana di masa depan untuk meningkatkan ketahanan bangunan.</p>
                        <li>Penyediaan Bantuan Kemanusiaan</li>
                        <p>Melanjutkan distribusi bantuan berupa makanan, air, obat-obatan, dan kebutuhan pokok lainnya kepada masyarakat yang terdampak, terutama mereka yang masih berada di tempat penampungan.</p>
                        <li>Perhatikan Instruksi Darurat</li>
                        <p>Perhatikan instruksi darurat saat terjadi letusan. Instruksi ini akan mengarahkan Anda harus dievakuasi ke tempat lain atau dapat tetap berada di tempat karena efek tak begitu besar. Korban letusan biasanya banyak berjatuhan akibat tidak mengindahkan instruksi darurat.</p>
                    </ol>
                    </p>
            
